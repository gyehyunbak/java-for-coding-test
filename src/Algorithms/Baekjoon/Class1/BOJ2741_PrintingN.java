package Algorithms.Baekjoon.Class1;

import java.util.Scanner;

public class BOJ2741_PrintingN {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for(int i = 1; i <= n; i++) {
            System.out.println(i);
        }
    }
}
